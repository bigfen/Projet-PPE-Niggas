				<article class="textgene">
					La FFTir forme et recrute des bénévoles pour renforcer ses équipes d'encadrement dans les clubs. Actuellement plus de 5 000 bénévoles agissent sur le terrain en faveur du développement du Tir sportif. 
					<br/>
					<h3>Quel est le role d'un brevet fédéral d'animateur de club ?</h3><br/>
					Il réalise, bénévolement, des actions d'accueil, d'animation et d'information du public, ainsi que des cycles de découverte de l'activité Tir sportif dans des conditions optimales de sécurité. Ses fonctions lui permettent :
					<ul>
						<li>d'accueillir et de délivrer des renseignements aux nouveaux adhérents,</li>
						<li>de participer au fonctionnement du club (prêts d'armes, organisation des séances d'entraînement...),</li>
						<li>d'encadrer des débutants dans leurs premières séances d'initiation,</li>
						<li>d'adhérer au projet de l'association dans laquelle il encadre.</li>
					</ul>
					<br/>
					<h3>Pourquoi devenir animateur de club ?</h3><br/>
					<ul>
						<li>pour le plaisir de partager sa passion avec d'autres et faire connaître le Tir sportif,</li>
						<li>pour améliorer ses connaissances afin de mieux les transmettre,</li>
						<li>pour valoriser son club et montrer que le Tir est un sport à part entière avec des encadrants formés et compétents.</li>
					</ul>
					<br/>
					<h3>Comment devenir animateur ?</h3><br/>
					Il faut suivre une formation dans votre région d'une durée globale de 60 heures se décomposant en :<br/>
					<ul>
						<li>20 heures de formation initiale théorique et pratique;</li>
						<li>40 heures de stage en situation dans un club,</li>
					</ul><br/>
					et satisfaire aux conditions d'obtention du diplôme.
				</p>
